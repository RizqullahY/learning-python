# bitwise,biner,binary

a = 9
b = 5 

print()
print('nilai : ',a,'binary : ',format(a,'08b'))

print('nilai : ',b,'binary : ',format(b,'08b'))

r = 255
print('nilai : ',r,'binary : ',format(r,'08b'))
print()

# bitwise OR (|)

print('---OR---')
c = a|b
print('nilai : ',c,'binary : ',format(c,'08b'))
print()

# bitwise AND (%)

print('---AND---')
c = a & b
print('nilai : ',c,'binary : ',format(c,'08b'))
print()

# bitwise XOR (^)

print('---XOR---')
c = a ^ b
print('nilai : ',c,'binary : ',format(c,'08b'))

# bitwise NOT (~)

print('---NOT---')

print()
c = ~ a
print('nilai : ',c,'binary : ',format(c,'08b'))
print()